  data () {
    return {
      loadPoint: 300,
      currentScrollY: window.scrollY,
      previousScrollY: window.scrollY,
      lazyload: false,
      trustpilot: false
    };
  },

  mounted () {
    if (!this.template.includes('product') && !this.tallEnough()) this.lazyload = true;

    window.addEventListener('scroll', this.lazyloaded);
    window.addEventListener('wheel', this.lazyloaded);
    window.addEventListener('touchstart', this.lazyloaded);
  },

  methods: {
    tallEnough () {
      const body = document.body;
      const html = document.documentElement;

      const height = Math.max(body.scrollHeight, body.offsetHeight,
        html.clientHeight, html.scrollHeight, html.offsetHeight) -
                     this.loadPoint;

      if (height <= window.innerHeight) {
        return false;
      } else {
        return true;
      }
    },

    lazyloaded () {
      if (this.lazyload) {
        if (this.trustpilot) return;

        this.loadTrustpilot();

        return;
      }

      this.currentScrollY = window.scrollY;

      if ((this.currentScrollY > this.loadPoint && this.currentScrollY > this.previousScrollY) || !this.tallEnough()) {
        this.lazyload = true;
        this.previousScrollY = this.currentScrollY;
      }
    },

    loadTrustpilot () {
      if (window.Trustpilot && document.getElementById('footer-trustbox')) {
        const element = document.getElementById('footer-trustbox');
        if (!element.classList.contains('loaded')) {
          window.Trustpilot.loadFromElement(element);
          element.classList.add('loaded');
          this.trustpilot = true;
        }
      }
    }
  },

  destroyed () {
    window.removeEventListener('scroll', this.lazyloaded);
    window.removeEventListener('wheel', this.lazyloaded);
    window.removeEventListener('touchstart', this.lazyloaded);
  }
